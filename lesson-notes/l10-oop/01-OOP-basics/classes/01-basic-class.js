// ============================================
// ПРИКЛАД 1: Простий клас Person
// ============================================

/**
 * Клас Person представляє людину з ім'ям та віком
 */
class Person {
    // Конструктор - спеціальний метод, який викликається при створенні нового об'єкта
    // Він використовується для ініціалізації властивостей об'єкта
    constructor(name, age) {
        // this вказує на поточний об'єкт, який створюється
        this.name = name;  // Публічна властивість - доступна ззовні
        this.age = age;    // Публічна властивість - доступна ззовні
    }

    // Метод - функція, яка належить класу
    // Методи визначають поведінку об'єктів
    greet() {
        console.log(`Привіт! Мене звати ${this.name} і мені ${this.age} років.`);
    }

    // Метод для отримання інформації про особу
    getInfo() {
        return `${this.name}, ${this.age} років`;
    }
}

// ============================================
// ВИКОРИСТАННЯ КЛАСУ
// ============================================

// Створення екземпляра (instance) класу Person
// Екземпляр - це конкретний об'єкт, створений на основі класу
const person1 = new Person("Олена", 25);
const person2 = new Person("Іван", 30);

// Виклик методів об'єкта
person1.greet();  // Виведе: Привіт! Мене звати Олена і мені 25 років.
person2.greet();  // Виведе: Привіт! Мене звати Іван і мені 30 років.

// Доступ до властивостей об'єкта
console.log(person1.name);  // Олена
console.log(person2.age);   // 30

// Використання методу getInfo
console.log(person1.getInfo());  // Олена, 25 років

