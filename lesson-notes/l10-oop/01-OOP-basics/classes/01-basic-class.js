/**
 * Клас Calculator для виконання математичних операцій
 */
class Calculator {
    constructor() {
        // Можна створити клас без параметрів
        this.history = [];  // Масив для зберігання історії операцій
    }

    // Метод для додавання
    add(a, b) {
        const result = a + b;
        this.history.push(`${a} + ${b} = ${result}`);
        return result;
    }

    // Метод для віднімання
    subtract(a, b) {
        const result = a - b;
        this.history.push(`${a} - ${b} = ${result}`);
        return result;
    }

    // Метод для множення
    multiply(a, b) {
        const result = a * b;
        this.history.push(`${a} * ${b} = ${result}`);
        return result;
    }

    // Метод для отримання історії операцій
    getHistory() {
        return this.history;
    }

    // Метод для очищення історії
    clearHistory() {
        this.history = [];
    }
}

// Використання класу Calculator
const calc = new Calculator();
console.log(calc.add(5, 3));        // 8
console.log(calc.subtract(10, 4));   // 6
console.log(calc.multiply(3, 4));    // 12
console.log(calc.getHistory());      // ['5 + 3 = 8', '10 - 4 = 6', '3 * 4 = 12']

